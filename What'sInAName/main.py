# Function to find the area
def find_area(height,width):
    area = height * width
    return area

# Function to find the volume
def find_volume(height2,width2,length):
    volume = height2 * width2 * length
    return volume

# Finds the area of the square
square_height = 5
square_width = 3
square_area = find_area(square_height,square_width)
print("The area of the square is",square_area)

# Finds the volume of the cube
cube_height = 4
cube_width = 6
cube_length = 2
cube_volume = find_volume(cube_height,cube_width,cube_length)
print("The area of the volume is",cube_volume)